#include "libft.h"

size_t	ft_strlcpy(char *dest, char *src, size_t size)
{
	int		l;
	size_t	i;

	i = 0;
	l = ft_strlen(src);
	if (size == 0)
		return (l);
	while (i < (size - 1) && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (l);
}
