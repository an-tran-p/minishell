#include "../minishell.h"

int	ft_pwd(void)
{
	char	cwd[PATH_MAX];

	if (getcwd(cwd, PATH_MAX))
	{
		ft_printf("%s\n", cwd);
		return (0);
	}
	else
		return (1);
}
